# 1.	Jenny's Secret Message
# Jenny studies programming with Python and wants to create a program that greets a user when he/she gives his/her
# name. Jenny however is in love with Johnny, and would like to greet him differently. Can you help her?
# Examples
# Input	    Output
# Peter	    Hello, Peter!
# Amy   	Hello, Amy!
# Johnny	Hello, my love!

name = input()

if name == "Johnny":
    print("Hello, my love!")
else:
    print(f"Hello, {name}!")




